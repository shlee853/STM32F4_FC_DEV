void USART_Transmit(USART_TypeDef *USARTx, uint8_t * data, uint16_t length){
    uint16_t i=0;
    for(i=0;i<length;i++){
        LL_USART_TransmitData8(USARTx,*(data+i));
        while(!LL_USART_IsActiveFlag_TXE(USARTx));
    }
}

